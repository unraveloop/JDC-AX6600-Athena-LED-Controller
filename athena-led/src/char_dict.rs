use std::collections::HashMap;
use once_cell::sync::Lazy;

pub static CHAR_DICT: Lazy<HashMap<char, Vec<u8>>> = Lazy::new(|| {
    let mut dict = HashMap::new();
    
    // --- Numbers (数字) ---
    dict.insert('0', vec![0b00011111, 0b00010001, 0b00011111]);
    dict.insert('1', vec![0b00010001, 0b00011111, 0b00010000]);
    dict.insert('2', vec![0b00011001, 0b00010101, 0b00010011]);
    dict.insert('3', vec![0b00010001, 0b00010101, 0b00011111]);
    dict.insert('4', vec![0b00000111, 0b00000100, 0b00011111]);
    dict.insert('5', vec![0b00010111, 0b00010101, 0b00011101]);
    dict.insert('6', vec![0b00011111, 0b00010101, 0b00011101]);
    dict.insert('7', vec![0b00000001, 0b00000001, 0b00011111]);
    dict.insert('8', vec![0b00011111, 0b00010101, 0b00011111]);
    dict.insert('9', vec![0b00010111, 0b00010101, 0b00011111]);
    
    // --- Letters (字母) ---
    // 🌟 [字体优化] A 加宽到 4 列，顶部圆角 + 完整横梁，远看不再和 M/H 混淆
    // .XX.
    // X..X
    // XXXX
    // X..X
    // X..X
    dict.insert('A', vec![0b00011110, 0b00000101, 0b00000101, 0b00011110]);
    dict.insert('B', vec![0b00011111, 0b00010101, 0b00001010]);
    dict.insert('C', vec![0b00001110, 0b00010001, 0b00010001]);
    dict.insert('D', vec![0b00011111, 0b00010001, 0b00001110]);
    dict.insert('E', vec![0b00011111, 0b00010101, 0b00010101]);
    dict.insert('F', vec![0b00011111, 0b00000101, 0b00000101]);
    dict.insert('G', vec![0b00001110, 0b00010001, 0b00011101]);
    dict.insert('H', vec![0b00011111, 0b00000100, 0b00011111]);
    dict.insert('I', vec![0b00010001, 0b00011111, 0b00010001]);
    dict.insert('J', vec![0b00011000, 0b00010000, 0b00001111]);
    dict.insert('K', vec![0b00011111, 0b00000100, 0b00011011]);
    dict.insert('L', vec![0b00011111, 0b00010000, 0b00010000]);
    dict.insert('M', vec![0b00011111, 0b00000010, 0b00011111]);
    // 🌟 [字体优化] N 加宽到 4 列，画出真正的斜对角线
    // 旧版 3 列的 N (左竖+断层) 几乎认不出来，用户吐槽最狠的就是它
    // X..X
    // XX.X
    // X.XX
    // X..X
    // X..X
    dict.insert('N', vec![0b00011111, 0b00000010, 0b00000100, 0b00011111]);
    dict.insert('O', vec![0b00001110, 0b00010001, 0b00001110]);
    dict.insert('P', vec![0b00011111, 0b00000101, 0b00000010]);
    dict.insert('Q', vec![0b00001110, 0b00010001, 0b00011110]);
    dict.insert('R', vec![0b00011111, 0b00000101, 0b00011010]);
    dict.insert('S', vec![0b00010010, 0b00010101, 0b00001001]);
    dict.insert('T', vec![0b00000001, 0b00011111, 0b00000001]);
    // 🌟 [字体修复] 旧版 U 和 V 字模一模一样 (都是尖底)！
    // U 改为方底 (两侧立柱到底 + 底边)，V 保持尖底，肉眼可分
    dict.insert('U', vec![0b00011111, 0b00010000, 0b00011111]);
    dict.insert('V', vec![0b00001111, 0b00010000, 0b00001111]);
    dict.insert('W', vec![0b00011111, 0b00001000, 0b00011111]);
    dict.insert('X', vec![0b00011011, 0b00000100, 0b00011011]);
    dict.insert('Y', vec![0b00000011, 0b00011100, 0b00000011]);
    dict.insert('Z', vec![0b00011001, 0b00010101, 0b00010011]);
    
    // --- Special characters (符号) ---
    dict.insert(' ', vec![0b00000000]); 

    dict.insert('-', vec![0b00000100, 0b00000100, 0b00000100]);
    dict.insert('_', vec![0b00010000, 0b00010000, 0b00010000]);
    dict.insert('=', vec![0b00001010, 0b00001010, 0b00001010]);
    dict.insert('+', vec![0b00000100, 0b00001110, 0b00000100]);
    dict.insert('*', vec![0b00001010, 0b00000100, 0b00001010]);
    dict.insert('/', vec![0b00011000, 0b00000110, 0b00000001]);
    dict.insert('\\', vec![0b00000001, 0b00000110, 0b00011000]);
    dict.insert('.', vec![0b00010000]);
    
    dict.insert(':', vec![0b00000000, 0b00001010, 0b00000000]);
    // [隐形冒号]
    dict.insert(';', vec![0b00000000, 0b00000000, 0b00000000]);
    // [新增] 紧凑版冒号！专供 time_sec 模式使用，只有一列宽！
    dict.insert('^', vec![0b00001010]);
    dict.insert('℃', vec![
        // 1. 左上角的“点” (2个像素高，实心)
        // 0b00000011 表示最上面两颗灯亮，看起来像个点
        0b00000011, 
        
        // 2. 中间空一列
        0b00000000,
        
        // 3. 右边的大写 C
        0b00001110, 0b00010001, 0b00010001 
    ]);

    // [新增] 波浪号 '~' (修复 1624 粘连问题)
    dict.insert('~', vec![0b00001000, 0b00010000, 0b00001000]);
    
    // [新增] 百分号 '%' (CPU/Mem 显示需要)
    dict.insert('%', vec![0b00011001, 0b00001000, 0b00010011]);

    // 🌟 [补齐缺失字符] 以前这些字符不在字模表里，遇到会直接“凭空消失”
    // '?' — 错误提示 (如 NIC 状态未知) 会用到
    // XXX
    // ..X
    // .X.
    // ...
    // .X.
    dict.insert('?', vec![0b00000001, 0b00010101, 0b00000011]);
    // '(' 与 ')' — 错误码 (如 W:Err(U)) 会用到
    dict.insert('(', vec![0b00001110, 0b00010001]);
    dict.insert(')', vec![0b00010001, 0b00001110]);

    // --- Weather Icons (已根据 W 字形翻转：Bit0=上, Bit4=下) ---

    // 1. 晴天 (Sun) - 旋转效果 (中心对称，改动不大，但微调了光芒)
    // Frame 1: ☀ (十字光芒)
    dict.insert('☀', vec![
        0b00000100, // ..*.. (Bit 2)
        0b00010101, // *.*.* (Bit 0,2,4)
        0b00001110, // .***. (Bit 1,2,3 - 核心)
        0b00010101, // *.*.*
        0b00000100  // ..*..
    ]);
    // Frame 2: ☼ (X形光芒)
    dict.insert('☼', vec![
        0b00010001, // *...* (Bit 0,4 - 角落)
        0b00001010, // .*.*. (Bit 1,3)
        0b00000100, // ..*.. (Bit 2 - 核心)
        0b00001010, // .*.*.
        0b00010001  // *...*
    ]);

    // 2. 下雨 (Rain) - 下落效果 (关键修正！)
    // 原理：伞盖在低位(上)，手柄在高位(下)，雨滴从低位往高位跑
    // Frame 1: ☂ (雨滴在半空)
    dict.insert('☂', vec![
        0b00010111, // Col 1: 云+雨(上/下)
        0b00000011, // Col 2: 纯云
        0b00010111, // Col 3: 云+雨(上/下)
        0b00000011, // Col 4: 纯云
        0b00010111  // Col 5: 云+雨(上/下)
    ]);

    // Frame B: 雨点在 4 行 (Bit 3)
    // 列 1,3,5: Cloud(3) + Row4(8) = 11 (0b00001011)
    // 列 2,4:   Cloud(3) = 3 (0b00000011)
    dict.insert('☔', vec![
        0b00001011, // Col 1: 云+雨(中)
        0b00000011, // Col 2: 纯云
        0b00001011, // Col 3: 云+雨(中)
        0b00000011, // Col 4: 纯云
        0b00001011  // Col 5: 云+雨(中)
    ]);

    // 3. 多云 (Cloud) - 飘动效果
    // 原理：云朵应该浮在上面 (Bit 0,1,2)
    // Frame 1: ☁ (靠左)
    dict.insert('☁', vec![
        0b00000110, // ..** (Bit 1,2 - 云左边)
        0b00001111, // **** (Bit 0-3 - 云身)
        0b00001111, // **** (Bit 0-3 - 云身)
        0b00000110, // ..** 0b00000000 
        0b00000000, 
    ]);
    // Frame 2: 🌥 (整体右移)
    dict.insert('🌥', vec![
        0b00000000, 
        0b00000110, // ..**
        0b00001111, // ****
        0b00001111, // ****
        0b00000110  // ..**
    ]);

    // 4. 雷暴 (Thunder) - 闪电形状
    // Frame 1: ⚡ (Z字形)
    dict.insert('⚡', vec![
        0b00000011, // ..** (上)
        0b00000110, // .**. (中)
        0b00011100, // ***. (闪电折角)
        0b00001000, // ...* (下)
        0b00010000  // ....* (底)
    ]);
    // Frame 2: ☇ (空心或微变，模拟闪烁)
    dict.insert('☇', vec![
        0b00000000, 
        0b00000110, 
        0b00001100, // 稍微变一下形状
        0b00001000, 
        0b00000000 
    ]);

    // 5. 雪 (Snow) - 飘落
    dict.insert('❄', vec![0b00010101, 0b00001110, 0b00011111, 0b00001110, 0b00010101]);
    dict.insert('❅', vec![0b00001010, 0b00010001, 0b00001110, 0b00010001, 0b00001010]);

    // 🌟 [修复] 6. 雾 (Fog) — wttr / open-meteo 会返回这个图标，
    // 以前不在字模表里，导致雾天时天气图标直接消失
    // 两条错开的横线，模拟雾气弥漫
    dict.insert('🌫', vec![0b00001010, 0b00001010, 0b00001010, 0b00001010, 0b00001010]);

    dict
});